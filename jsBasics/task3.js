// Завдання 3: Числа та булі

// Створення змінної для віку особи
let age = 18;  // Сюди підставляти числа для тестування, наприклад по черзі ввести 17, 18, 19

// Створення булевої змінної для визначення, чи є особа повнолітньою
let isAdult = age >= 18;  // Якщо вік 18 або більше, то особа повнолітня

// Виведення змінних на консоль
console.log("Age: " + age);  // Виводимо вік
console.log("Is Adult: " + isAdult);  // Виводимо, чи є особа повнолітньою