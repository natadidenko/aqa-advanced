// Завдання 4.1: Площа кола
let radius = 10;  

// Обчислення площі кола
let areaOfCircle = Math.PI * radius ** 2;  // Використовуємо Math.PI для числа пі

console.log("Area of Circle: " + areaOfCircle.toFixed(2));  // Округлюємо до 2 знаків після коми


// Завдання 4.2: Площа прямокутника
let length = 10;  
let width = 12;   

// Обчислення площі прямокутника
let areaOfRectangle = length * width; 

console.log("Area of Rectangle: " + areaOfRectangle);


// Завдання 4.3: Об'єм циліндра
let cylinderRadius = 9;  
let height = 13;          

// Обчислення об'єму циліндра
let volumeOfCylinder = Math.PI * cylinderRadius ** 2 * height;  

console.log("Volume of Cylinder: " + volumeOfCylinder.toFixed(2));  // Округлюємо до 2 знаків після коми
